// calcuteTax
function calculateTax(amount) {
    const taxRate = 0.1;
    return amount * taxRate;
}

// convertToUpperCase
function convertToUpperCase(text) {
    return text.toUpperCase();
}

// findMaximum
function findMaximum(num1, num2) {
    if (num1 > num2) {
        return num1;
    } else {
        return num2;
    }
}